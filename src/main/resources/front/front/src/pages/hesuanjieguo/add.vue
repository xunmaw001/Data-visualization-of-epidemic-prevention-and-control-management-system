          </el-form-item>
          <el-form-item :style='{"padding":"10px","boxShadow":"0 2px 0 #efe4d4","margin":"0 0 10px"}' label="用户姓名" prop="yonghuxingming">
            <el-input v-model="ruleForm.yonghuxingming" 
                placeholder="用户姓名" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","boxShadow":"0 2px 0 #efe4d4","margin":"0 0 10px"}' label="人员账号" prop="renyuanzhanghao">
            <el-input v-model="ruleForm.renyuanzhanghao" 
                placeholder="人员账号" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","boxShadow":"0 2px 0 #efe4d4","margin":"0 0 10px"}' label="防疫人员" prop="fangyirenyuan">
            <el-input v-model="ruleForm.fangyirenyuan" 
                placeholder="防疫人员" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","boxShadow":"0 2px 0 #efe4d4","margin":"0 0 10px"}' label="结果时间" prop="jieguoshijian" >
              <el-date-picker
                  value-format="yyyy-MM-dd HH:mm:ss"
