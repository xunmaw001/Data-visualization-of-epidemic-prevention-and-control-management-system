<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            yuyuebianhao : false,
            jiancedianmingcheng : false,
            jiancedianweizhi : false,
            yonghuzhanghao : false,
            yonghuxingming : false,
            renyuanzhanghao : false,
            fangyirenyuan : false,
            jieguoshijian : false,
            hesuanjieguo : false,
            crossuserid : false,
            crossrefid : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          yuyuebianhao: '',
          jiancedianmingcheng: '',
          jiancedianweizhi: '',
          yonghuzhanghao: '',
          yonghuxingming: '',
          renyuanzhanghao: '',
          fangyirenyuan: '',
          jieguoshijian: '',
          hesuanjieguo: '',
          crossuserid: '',
          crossrefid: '',
        },
        hesuanjieguoOptions: [],
        rules: {
          yuyuebianhao: [
          ],
          jiancedianmingcheng: [
          ],
          jiancedianweizhi: [
          ],
          yonghuzhanghao: [
          ],
          yonghuxingming: [
          ],
          renyuanzhanghao: [
          ],
          fangyirenyuan: [
          ],
          jieguoshijian: [
          ],
          hesuanjieguo: [
          ],
          crossuserid: [
          ],
          crossrefid: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.jieguoshijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='yuyuebianhao'){
              this.ruleForm.yuyuebianhao = obj[o];
              this.ro.yuyuebianhao = true;
              continue;
            }
            if(o=='jiancedianmingcheng'){
              this.ruleForm.jiancedianmingcheng = obj[o];
              this.ro.jiancedianmingcheng = true;
              continue;
            }
            if(o=='jiancedianweizhi'){
              this.ruleForm.jiancedianweizhi = obj[o];
              this.ro.jiancedianweizhi = true;
              continue;
            }
            if(o=='yonghuzhanghao'){
              this.ruleForm.yonghuzhanghao = obj[o];
              this.ro.yonghuzhanghao = true;
              continue;
            }
            if(o=='yonghuxingming'){
              this.ruleForm.yonghuxingming = obj[o];
              this.ro.yonghuxingming = true;
              continue;
            }
            if(o=='renyuanzhanghao'){
              this.ruleForm.renyuanzhanghao = obj[o];
              this.ro.renyuanzhanghao = true;
              continue;
            }
            if(o=='fangyirenyuan'){
              this.ruleForm.fangyirenyuan = obj[o];
              this.ro.fangyirenyuan = true;
              continue;
            }
            if(o=='jieguoshijian'){
              this.ruleForm.jieguoshijian = obj[o];
              this.ro.jieguoshijian = true;
              continue;
            }
            if(o=='hesuanjieguo'){
              this.ruleForm.hesuanjieguo = obj[o];
              this.ro.hesuanjieguo = true;
              continue;
            }
            if(o=='crossuserid'){
              this.ruleForm.crossuserid = obj[o];
              this.ro.crossuserid = true;
              continue;
            }
            if(o=='crossrefid'){
              this.ruleForm.crossrefid = obj[o];
              this.ro.crossrefid = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if(json.renyuanzhanghao!=''&&json.renyuanzhanghao){
                this.ruleForm.renyuanzhanghao = json.renyuanzhanghao
            }
            if(json.fangyirenyuan!=''&&json.fangyirenyuan){
                this.ruleForm.fangyirenyuan = json.fangyirenyuan
            }
          }
        });
        this.hesuanjieguoOptions = "阴性,阳性".split(',')
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('hesuanjieguo/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('hesuanjieguo/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('hesuanjieguo/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('hesuanjieguo/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

